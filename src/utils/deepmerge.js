// https://github.com/KyleAMathews/deepmerge
function emptyTarget(val) {
	return Array.isArray(val) ? [] : {}
}
const clone = (value, options) => deepmergeMaster(emptyTarget(value), value, options);

export function oldArrayMerge(target, source, optionsArgument) {
  const destination = target.slice();

  source.forEach((e, i) => {
    if (typeof destination[i] === 'undefined') {
      const cloneRequested = !optionsArgument || optionsArgument.clone !== false;
      const shouldClone = cloneRequested && isMergeableObject(e);
      destination[i] = shouldClone ? clone(e, optionsArgument) : e;
    } else if (isMergeableObject(e)) {
      destination[i] = deepmergeMaster(target[i], e, optionsArgument);
    } else if (target.indexOf(e) === -1) {
      destination.push(e);
    }
  });
  return destination;
}

